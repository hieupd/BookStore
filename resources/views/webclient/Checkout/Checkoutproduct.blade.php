        function qtyDown(id) {
            var qty_el = document.getElementById('cart[' + id + '][qty]');
            var qty = qty_el.value;
            if (!isNaN(qty) && qty > 1) {
                qty_el.value--;
            }
            return false;
        }

        function qtyUp(id) {
            var qty_el = document.getElementById('cart[' + id + '][qty]');
            var qty = qty_el.value;
            if (!isNaN(qty)) {
                qty_el.value++;
            }
            return false;
        }
